    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        usuario.setEmailUsuario(campoLogin.getText());
        usuario.setNomeUsuario(campoNome.getText());
        usuario.setSenhaUsuario(campoSenha.getText());
        if(usuario.getId() != 0){
            dao.editarUsuario(usuario);
            JOptionPane.showMessageDialog(null, "Usu치rio editado com sucesso");
            adm.setVisible(true);
            dispose();
        }else{
            dao.salvarUsuario(usuario);
            JOptionPane.showMessageDialog(null, "Usu치rio salvo com sucesso");
            limparCampos();
        }
        


    }//GEN-LAST:event_btnCadastrarActionPerformed

